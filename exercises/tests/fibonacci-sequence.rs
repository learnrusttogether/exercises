use fibonacci_sequence::*;

#[test]
fn test_fibonacci_sequence() {
    let a = vec![
        [0, 0],
        [1, 1],
        [2, 1],
        [4, 3],
        [20, 6765],
        [23, 28657],
        [25, 75025],
        [27, 196418],
        [39, 63245986],
        [40, 102334155],
    ];

    for i in a {
        assert_eq!(i[1], fibonacci(i[0]));
    }
}
